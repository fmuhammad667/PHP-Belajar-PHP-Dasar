<?php

// operator perbandingan digunakan untuk membandingkan dua buah value atau variable
// hasil dari operator perbandingan adalah boolean, true jika benar dan false jika salah
// ini mirip dengan studi matematika

// sama dengan (konversi : Type juggling)
var_dump("10" == 10);  // (true)

// identik
var_dump("10" === 10); // (false)

// tidak sama identik
var_dump("10" !== 10); // true

// tidak sama dengan
var_dump("10" != 10); // (false)

// kurang dari 
var_dump(10 < 9); // (false)

// lebih dari atau sama dengan
var_dump(9 >= 9); // (true)


