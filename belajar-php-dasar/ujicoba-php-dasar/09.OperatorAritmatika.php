<?php

/* 
Operator Aritmatika
angka positif dan negatif
pertambahan, pengurangan, perkalian, pembagian, sisa bagi, dan pangkat
*/

$a = 16;
$b = 8;

// pertambahan
$pertambahan = $a + $b;
var_dump($pertambahan);

// pengurangan
$pengurangan = $b - $a;
var_dump($pengurangan);

// perkalian
$perkalian = $a * $b;
var_dump($perkalian);

// pembagian
$pembagian = $a / $b;
var_dump($pembagian);

// sisa bagi
$sisabagi = $a % 3;
var_dump($sisabagi);

// pangkat
$pangkat = $b ** 4;
var_dump($pangkat);

$minus = -$b;
var_dump($minus);

$plus = +$b;
var_dump($plus);